    public function getAllYogis(Request $request)
    {
        $yogis = auth()->user()->stateRegion
            ->yogis()
            ->where('yogis.name', 'like', '%' . $request->q . '%')
            ->orderBy('id', 'desc')
            ->paginate(5);

        return response()->json(new YogiSelect2ResourceCollection($yogis), 200);
    }

